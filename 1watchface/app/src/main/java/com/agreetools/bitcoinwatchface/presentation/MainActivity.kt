class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        installSplashScreen()

        super.onCreate(savedInstanceState)

        // Using ExampleUtils from shared module
//        val message = ExampleUtils.greet()
//        Log.d("SharedModuleTest", message) // Use a custom tag here

        CoroutineScope(Dispatchers.IO).launch {
            val price = BitcoinPriceFetcher.fetchCurrentPrice()
            Log.d("BitcoinPrice", "Current BTC Price: $price")
        }

        setTheme(android.R.style.Theme_DeviceDefault)

        setContent {
            WearApp("Android")
        }
    }
}

@Composable
fun WearApp(greetingName: String) {
    AgreeToolsBitcoinWatchfaceTheme {
        Box(
            modifier = Modifier
                .fillMaxSize()
                .background(MaterialTheme.colors.background),
            contentAlignment = Alignment.Center
        ) {
            TimeText()
            Greeting(greetingName = greetingName)
        }
    }
}

@Composable
fun Greeting(greetingName: String) {
    Text(
        modifier = Modifier.fillMaxWidth(),
        textAlign = TextAlign.Center,
        color = MaterialTheme.colors.primary,
        text = stringResource(R.string.hello_world, greetingName)
    )
}

